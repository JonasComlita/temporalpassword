from django.db import models
from django.conf import settings
from django.utils import timezone
import uuid
from datetime import timedelta
import pyotp
from django.core.validators import MinLengthValidator

class EmailVerificationToken(models.Model):
    """Stores tokens for verifying user email addresses."""
    id = models.UUIDField(primary_key=True, default=uuid.uuid4, editable=False)
    user = models.ForeignKey(settings.AUTH_USER_MODEL, on_delete=models.CASCADE)
    created_at = models.DateTimeField(auto_now_add=True)

    def __str__(self):
        return f"Verification token for {self.user.email}"

    @property
    def is_expired(self):
        """Check if the token is older than the expiry duration (e.g., 1 hour)."""
        expiry_duration = timedelta(hours=1) # Make this configurable in settings if needed
        return timezone.now() > self.created_at + expiry_duration
    
